#include <iostream>

using namespace std;

int main()
{
    int a;
    cout<<"Size of int is : "<<sizeof(a)<<"bytes"<<endl;
    float b;
    cout<<"Size of float is : "<<sizeof(b)<<"bytes"<<endl;
    char c;
    cout<<"Size of char is : "<<sizeof(c)<<"bytes"<<endl;
    double d;
    cout<<"Size of double is : "<<sizeof(d)<<"bytes"<<endl;

    return 0;
}
